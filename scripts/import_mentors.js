// 批量导入函数
async function importMentors() {
  console.log(`开始导入${mentorsData.length}位导师数据...`)
  
  for (let i = 0; i < mentorsData.length; i++) {
    const mentor = mentorsData[i]
    try {
      await db.collection('mentors').add({
        data: mentor
      })
      console.log(`✅ 导入成功：${mentor.name}`)
    } catch (e) {
      console.log(`❌ 导入失败：${mentor.name}，错误：${e.message}`)
    }
  }
  
  console.log('所有导师数据导入完成！')
}

// 执行导入
importMentors()
